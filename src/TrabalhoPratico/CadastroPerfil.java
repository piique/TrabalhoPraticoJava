    private void buttonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCadastrarActionPerformed
        try {

            Diretorio diretorio = new Diretorio();
            String file = diretorio.getUsuarios();

            ManipuladorArquivo a = new ManipuladorArquivo(file);
            try {

                a.escritor(txtUsuario.getText() + ";"
                        + txtSenha.getText() + ";"
                        + txtNome.getText() + ";"
                        + txtIdade.getText() + ";"
                        + txtEmail.getText() + ";"
                        + txtPeso.getText() + ";"
                        + txtAltura.getText() + ";" + 0);

                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso");
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Escreva apenas numeoros nos campos altura e peso");
                txtPeso.setText("");
                txtAltura.setText("");
            }
            setVisible(false);
            dispose();
        } catch (IOException ex) {
            Logger.getLogger(CadastroPerfil.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_buttonCadastrarActionPerformed

